package businessLayer;

import java.util.ArrayList;

/**
 * The Class BookBLL.
 */
public class BookBLL {
	
	private String author;
	private String bookTitle;
	private String iSBN;
	private String publisher;
	private String bDEDCP;
	private String note;
	private boolean required;
	private boolean latestEdition;
	
	private ArrayList<String> sections;
	
	/**
	 * To string.
	 *
	 * @return the string
	 */
	public String toString() {
		String thisObject = "********************\n" +
						    "Author: " + author + "\n" + 
						    "Title: " + bookTitle + "\n" +
						    "Publisher: " + publisher + "\n" +
						    "ISBN: " + iSBN + "\n" +
							"BD/ED/CP: " + bDEDCP + "\n" +
							"Required: " + required + "\n" +
							"Note: " + note + "\n" +
							"Sections : ";
							
							for(String temp: sections){
								thisObject += temp + ", ";
							}
		return thisObject;
	}
	
	/**
	 * Gets the author.
	 *
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}
	
	/**
	 * Sets the author.
	 *
	 * @param author the new author
	 */
	public void setAuthor(String author) {
		this.author = author;
	}
	
	/**
	 * Gets the book title.
	 *
	 * @return the book title
	 */
	public String getBookTitle() {
		return bookTitle;
	}
	
	/**
	 * Sets the book title.
	 *
	 * @param bookTitle the new book title
	 */
	public void setBookTitle(String bookTitle) {
		this.bookTitle = bookTitle;
	}
	
	/**
	 * Gets the isbn.
	 *
	 * @return the isbn
	 */
	public String getISBN() {
		return iSBN;
	}
	
	/**
	 * Sets the isbn.
	 *
	 * @param iSBN the new isbn
	 */
	public void setISBN(String iSBN) {
		this.iSBN = iSBN;
	}
	
	/**
	 * Gets the publisher.
	 *
	 * @return the publisher
	 */
	public String getPublisher() {
		return publisher;
	}
	
	/**
	 * Sets the publisher.
	 *
	 * @param publisher the new publisher
	 */
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	
	/**
	 * Gets the bdedcp.
	 *
	 * @return the bdedcp
	 */
	public String getBDEDCP() {
		return bDEDCP;
	}
	
	/**
	 * Sets the bdedcp.
	 *
	 * @param bDEDCP the new bdedcp
	 */
	public void setBDEDCP(String bDEDCP) {
		this.bDEDCP = bDEDCP;
	}
	
	/**
	 * Gets the note.
	 *
	 * @return the note
	 */
	public String getNote() {
		return note;
	}
	
	/**
	 * Sets the note.
	 *
	 * @param note the new note
	 */
	public void setNote(String note) {
		this.note = note;
	}
	
	/**
	 * Checks if is required.
	 *
	 * @return true, if is required
	 */
	public boolean isRequired() {
		return required;
	}
	
	/**
	 * Sets the required.
	 *
	 * @param required the new required
	 */
	public void setRequired(boolean required) {
		this.required = required;
	}
	
	/**
	 * Checks if is latest edition.
	 *
	 * @return true, if is latest edition
	 */
	public boolean isLatestEdition() {
		return latestEdition;
	}
	
	/**
	 * Sets the latest edition.
	 *
	 * @param latestEdition the new latest edition
	 */
	public void setLatestEdition(boolean latestEdition) {
		this.latestEdition = latestEdition;
	}
	
	/**
	 * Gets the sections.
	 *
	 * @return the sections
	 */
	public ArrayList<String> getSections() {
		return sections;
	}
	
	/**
	 * Sets the sections.
	 *
	 * @param sections the new sections
	 */
	public void setSections(ArrayList<String> sections) {
		this.sections = sections;
	}
}